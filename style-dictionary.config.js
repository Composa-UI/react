/**
 * The primitive platform reads only the primitive source so dark $extensions are
 * available; the semantic/js/swift platforms read primitive + semantic so that
 * {figma.composa.*} references resolve.
 */
async function run() {
  // Pass 1: primitives only (keeps $extensions, no unresolved references).
  const sdPrimitive = new StyleDictionary({
    source: ["tokens/primitive/**/*.json"],
    platforms: { primitiveCss: config.platforms.primitiveCss },
  });
  await sdPrimitive.buildAllPlatforms();

  // Pass 2: primitive + semantic together so references resolve.
  const sdAll = new StyleDictionary({
    source: ["tokens/primitive/**/*.json", "tokens/semantic/**/*.json"],
    platforms: {
      semanticCss: config.platforms.semanticCss,
      coreJson: config.platforms.coreJson,
      js: config.platforms.js,
      swift: config.platforms.swift,
    },
  });
  await sdAll.buildAllPlatforms();
}

run().then(
  () => {
    // eslint-disable-next-line no-console
    console.log("build:tokens — Style Dictionary build complete.");
  },
  (err) => {
    // eslint-disable-next-line no-console
    console.error(err);
    process.exit(1);
  }
);

export default config;
