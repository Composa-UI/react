#!/usr/bin/env node
// Generate a React-18-compatible types entry (src/react/index.react18.d.ts).
// ---------------------------------------------------------------------------
// Why this exists
// ---------------------------------------------------------------------------
// The canonical types (index.d.ts) declare every component as a function
// `(props: XProps) => React.ReactElement`. Under @types/react@18 some consumer
// tsconfigs (strict JSX / exactOptionalPropertyTypes) reject a bare
// ReactElement-returning function as a JSX component type, forcing them to wrap
// every export in an `asFC()` cast. This generated entry re-types each component
// as `React.FC<XProps>` — a shape @types/react@18 accepts directly in JSX — and
// re-exports all the prop interfaces and helper types unchanged, so React-18
// consumers can point their types at this file and drop the casts.
//
// It is GENERATED from index.d.ts so the two never drift. Run via `build:css`
// (and `check`); the output is committed. Do not hand-edit index.react18.d.ts.
// ---------------------------------------------------------------------------

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const SRC = resolve(root, "src/react/index.d.ts");
const OUT = resolve(root, "src/react/index.react18.d.ts");

const text = readFileSync(SRC, "utf8");

// Match `export declare function Name(props: NameProps): React.ReactElement[ | null];`
const componentRe =
  /export declare function ([A-Za-z0-9_]+)\(props: ([A-Za-z0-9_]+)\): React\.ReactElement(?: \| null)?;/g;

const components = [];
let m;
while ((m = componentRe.exec(text)) !== null) {
  components.push({ name: m[1], props: m[2] });
}

if (components.length === 0) {
  console.error("build-react18-types: no component declarations matched — aborting.");
  process.exit(1);
}

const header = `// AUTO-GENERATED by tools/build-react18-types.mjs — DO NOT EDIT BY HAND.
//
// React-18-compatible types entry. Re-exports every type from the canonical
// "./index.js" declarations, then re-types each component as React.FunctionComponent
// so @types/react@18 accepts them directly in JSX (no \`asFC()\` cast needed).
//
// Consumers on React 18 can target this file, e.g. via a tsconfig paths entry
// or the package's "./react18" types export.

import type * as React from "react";

// Re-export all interfaces, type aliases, and non-component values (e.g.
// createComposaComponents, the default \`components\` object) unchanged.
export * from "./index.js";
`;

// `export *` re-exports the component VALUES too; we must shadow each with an
// FC-typed redeclaration. A local re-import of the original prop types lets us
// build the FC<Props> signatures without restating the interfaces.
const propImports = Array.from(new Set(components.map((c) => c.props)))
  .sort()
  .join(",\n  ");

const importBlock = `\nimport type {\n  ${propImports},\n} from "./index.js";\n`;

const reexport = components
  .map((c) => `export declare const ${c.name}: React.FC<${c.props}>;`)
  .join("\n");

const output = header + importBlock + "\n" + reexport + "\n";
const rel = OUT.replace(root + "/", "");

// `--check` verifies the committed file is in sync with index.d.ts without
// rewriting it (used by `npm run check` so drift fails CI instead of silently
// shipping stale React-18 types).
if (process.argv.includes("--check")) {
  let existing = "";
  try {
    existing = readFileSync(OUT, "utf8");
  } catch {
    console.error(`build-react18-types --check: ${rel} is missing. Run \`node tools/build-react18-types.mjs\`.`);
    process.exit(1);
  }
  if (existing !== output) {
    console.error(`build-react18-types --check: ${rel} is stale. Run \`node tools/build-react18-types.mjs\` and commit.`);
    process.exit(1);
  }
  console.log(`build-react18-types --check — ${rel} is in sync (${components.length} components).`);
  process.exit(0);
}

writeFileSync(OUT, output, "utf8");
console.log(`build-react18-types — wrote ${rel} (${components.length} components).`);
