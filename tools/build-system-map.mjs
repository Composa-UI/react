// Generate specs/_system-map.md — the Phase-0 inventory for the doc-first rebuild.
//
// "Know what we have, what depends on what, and what's missing" before writing a
// single spec. This scans:
//   - design/component-api.json   → the component families + support components
//   - src/react/factory.js        → real dependency edges (which component renders
//                                    which, via `h(Other, …)`)
// and computes a tier per component by dependency depth (atoms depend on nothing
// but primitives; molecules depend on atoms; organisms on molecules). It also
// marks whether a spec exists yet (specs/<tier>/<name>.md) — all "missing" at start.
//
//   node tools/build-system-map.mjs

import fs from "node:fs";

const api = JSON.parse(fs.readFileSync(new URL("../design/component-api.json", import.meta.url), "utf8"));
const factory = fs.readFileSync(new URL("../src/react/factory.js", import.meta.url), "utf8");
const lines = factory.split("\n");

// Infrastructure primitives — excluded from tier depth (they're plumbing, not atoms).
const INFRA = new Set(["OverlayHost", "OverlayPortal", "OverlayLayer"]);

// Component function declarations at the factory's 2-space indent.
const decls = [];
lines.forEach((ln, i) => {
  const m = ln.match(/^ {2}function ([A-Z]\w+)\s*\(/);
  if (m) decls.push({ name: m[1], line: i });
});

const families = api.componentFamilies || {};
const support = api.supportComponents || {};
const allNames = new Set([...decls.map((d) => d.name), ...Object.keys(families), ...Object.keys(support)]);

// Dependency edges: within each function body, which OTHER components are rendered
// via `h(Name`. Precise enough to avoid comment/substring false positives.
const deps = {};
decls.forEach((d, idx) => {
  const end = idx + 1 < decls.length ? decls[idx + 1].line : lines.length;
  const body = lines.slice(d.line + 1, end).join("\n");
  const set = new Set();
  for (const name of allNames) {
    if (name === d.name) continue;
    if (new RegExp(`h\\(${name}[,)\\s]`).test(body)) set.add(name);
  }
  deps[d.name] = [...set];
});

// Tier = dependency depth, ignoring infra primitives. Cycle-guarded.
const depthCache = new Map();
function depth(name, seen = new Set()) {
  if (depthCache.has(name)) return depthCache.get(name);
  if (seen.has(name)) return 0; // cycle guard
  seen.add(name);
  const children = (deps[name] || []).filter((c) => !INFRA.has(c) && c !== name);
  const d = children.length ? 1 + Math.max(...children.map((c) => depth(c, new Set(seen)))) : 0;
  depthCache.set(name, d);
  return d;
}

const TIER_NAME = ["atoms", "molecules", "organisms", "organisms", "organisms"];
const tierLabel = (d) => TIER_NAME[Math.min(d, TIER_NAME.length - 1)];

// Build rows for every named component we can place.
const rows = [...allNames]
  .filter((n) => !INFRA.has(n))
  .map((name) => {
    const fam = families[name];
    const d = deps[name] !== undefined ? depth(name) : 0;
    const tier = INFRA.has(name) ? "primitive" : tierLabel(d);
    const specPath = `specs/${tier}/${name.toLowerCase()}.md`;
    const hasSpec = fs.existsSync(new URL(`../${specPath}`, import.meta.url));
    return {
      name,
      tier,
      depth: d,
      figma: fam ? (fam.figma || []).join(", ") : "",
      variants: fam ? fam.variantCount : "",
      deps: (deps[name] || []).filter((c) => !INFRA.has(c)),
      usesInfra: (deps[name] || []).filter((c) => INFRA.has(c)),
      spec: hasSpec ? "✅" : "—",
    };
  })
  .sort((a, b) => a.depth - b.depth || a.name.localeCompare(b.name));

const section = (title, predicate) => {
  const items = rows.filter(predicate);
  if (!items.length) return "";
  const body = items
    .map((r) => {
      const dep = r.deps.length ? ` ← ${r.deps.join(", ")}` : "";
      const infra = r.usesInfra.length ? ` _(needs ${r.usesInfra.join("/")})_` : "";
      const meta = r.variants ? ` · ${r.variants} variants` : "";
      const fig = r.figma ? ` · ${r.figma}` : "";
      return `| \`${r.name}\` | ${r.spec} |${dep || " —"}${infra} |${meta}${fig} |`;
    })
    .join("\n");
  return `\n### ${title}\n\n| Component | Spec | Depends on | Notes |\n|---|---|---|---|\n${body}\n`;
};

const out = `<!-- GENERATED by tools/build-system-map.mjs. A v1 inventory to refine, not gospel.
     Dependency edges are scanned from h(Name) calls in factory.js; tiers are computed
     by dependency depth. Hand-refine tiers/edges as specs are written. -->

# Composa — system map (Phase 0 inventory)

The doc-first rebuild starts here: **what we have, what depends on what, what's missing.**
No spec is written until this map is trusted. \`Spec\` shows whether a \`specs/<tier>/<name>.md\`
exists yet (all missing at start). \`Depends on\` = components rendered inside it (from a
factory scan). Overlay primitives (\`OverlayHost\`/\`Portal\`/\`Layer\`) are plumbing and are noted
as _needs_, not counted toward tier depth.

**Counts:** ${rows.length} components — ${rows.filter((r) => r.tier === "atoms").length} atoms,
${rows.filter((r) => r.tier === "molecules").length} molecules,
${rows.filter((r) => r.tier === "organisms").length} organisms.
Specs written: ${rows.filter((r) => r.spec === "✅").length} / ${rows.length}.

## Foundations & tokens (write these FIRST — everything depends on them)

Not components — the substrate every spec references. Author as \`specs/foundations/*.md\`:
color · typography · spacing · radius · **elevation** · motion · z-index · iconography ·
accessibility · breakpoints · grid · borders · opacity. Token values live in \`design/tokens.css\`
(closed set; specs reference names, never raw values).

## Components by tier (write in this order: atoms → molecules → organisms)
${section("Atoms (depend on nothing but primitives)", (r) => r.tier === "atoms")}
${section("Molecules (compose atoms)", (r) => r.tier === "molecules")}
${section("Organisms (compose molecules — product surfaces)", (r) => r.tier === "organisms")}

## Infrastructure primitives

\`OverlayHost\` · \`OverlayPortal\` · \`OverlayLayer\` — the portal/mount plumbing every
tooltip, menu, and dialog depends on. Spec these alongside foundations; they're a hard
dependency for a large share of the tree.
`;

fs.writeFileSync(new URL("../specs/_system-map.md", import.meta.url), out);
console.log(`Wrote specs/_system-map.md — ${rows.length} components mapped.`);
